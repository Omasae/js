function getRandomDataFromArray(dataArray, startDate) {
  var currentDate = new Date();
  var timeDiff = Math.abs(currentDate.getTime() - startDate.getTime());
  var elapsedDays = Math.floor(timeDiff / (1000 * 3600 * 24));
  var randomIndex = elapsedDays % dataArray.length;
  return dataArray[randomIndex];
}

